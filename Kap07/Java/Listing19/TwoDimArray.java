public class TwoDimArray {
    public static void main(String[] args) {
        int[][] zahlArray = new int[3][2];

        zahlArray[0][0] = 11;
        zahlArray[0][1] = 12;
        zahlArray[1][0] = 21;
        zahlArray[1][1] = 22;
        zahlArray[2][0] = 31;
        zahlArray[2][1] = 32;
    } 
}